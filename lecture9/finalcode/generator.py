"""
你需要创建一个名为 `sheep_counter.py` 的程序。 这个程序的作用是帮助入睡，通过生成一系列绵羊来帮助数数。
具体要求如下：
1.  **输入：** 程序首先询问用户想要数多少只绵羊，获取一个整数 `n`。
2.  **生成绵羊：** 程序使用一个生成器函数，根据用户输入的数字 `n`，生成 `n` 行绵羊。每一行都包含一定数量的绵羊 emoji (🐑)。第一行没有绵羊，第二行一只，第三行两只，以此类推。
3.  **输出：** 程序将生成的每一行绵羊都打印到屏幕上。
请注意， 你需要使用生成器函数来生成绵羊序列， 然后打印每一行。
"""
def main():
    n = int(input("What's n? "))
    for s in sheep(n):
        print(s)

def sheep(n):
    for i in range(n):
        yield "🐑" * i

if __name__ == "__main__":
    main()
