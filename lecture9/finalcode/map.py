"""
具体要求如下：
1.  **核心功能：喊话（yell）**
    *   程序需要有一个喊话的功能，可以接收**任意数量**的词语。
2.  **接收方式：不限数量参数**
    *   喊话函数需要能够接收**任意数量**的参数。
3.  **喊话方式：大写**
    *   喊话的时候，要把每个词语都转换成大写字母。**请务必使用 `map` 函数来实现。**
4.  **最终效果：**
    *   程序最终会把转换成大写字母的词语喊出来（输出到屏幕上）。
请注意，程序需要能够接收任意数量的词语，并且**使用 `map` 函数**把它们都转换成大写字母后输出。 另外函数需要支持接收**任意数量**的参数。
"""
def yell(*words):
    uppercased = map(str.upper, words)
    print(*uppercased)

yell("This", "is", "CS50")