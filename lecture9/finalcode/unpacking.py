"""
这个计算器需要满足以下需求：
1.  **货币体系：** 在哈利波特的世界里，有三种货币：加隆 (Galleon)、西可 (Sickle) 和纳特 (Knut)。 它们之间的换算关系是：
    *   1 加隆 = 17 西可
    *   1 西可 = 29 纳特
2.  **计算总纳特：** 需要一个函数，接受加隆、西可和纳特的数量作为输入，计算出总共有多少纳特。
3.  **现有金额：** 我们已经有了一些现有的各种货币，存储在一个dict里。
4.  **总金额计算并展示：**
    *   使用现有的金额数据，调用货币计算函数，计算出总共有多少纳特。
    *   使用dict的解包
总而言之，这个计算器需要能够将哈利波特魔法世界的货币转换成统一的纳特单位，并展示总金额。
"""

def total(galleons, sickles, knuts):
    return (galleons * 17 + sickles) * 29 + knuts

coins = {"galleons": 100, "sickles": 50, "knuts": 25}

print(total(**coins), "Knuts")